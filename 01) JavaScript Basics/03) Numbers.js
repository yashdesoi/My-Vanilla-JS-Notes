// While operation, javascript follows B I D M A S, where I represents power. In followeing example
// First whatever inside brackets will be solved, then power(I) and lastly multiplication(I).
let result = 5 * (10 - 3)**2;
console.log(result);


// INCEREMENT OPERATOR
let i = 10;

let j = i++;

console.log('j =',j);
console.log('i =',i);
// In above example i++ return the previous value of i, and then upgrades it to i + 1. We can also
// do this without storing it in j or some other variable.
i++;
console.log('i =',i);

// We can also perform following operations.
let num = 10;

// num = num + 10;
num += 10;
console.log('num =',num);

// num = num - 5;
num -= 5;
console.log('num =',num);

// num = num / 3;
num /= 3;
console.log('num =',num);

// num = num * 6;
num *= 6;
console.log('num =',num);

